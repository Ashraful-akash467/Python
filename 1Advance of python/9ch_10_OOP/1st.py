class employ :
    language ="Py"     #This is a class attribute 
    salary=1200000


akash =employ()
akash.name="Akash"          #This is an instance attribute 
print(akash.name,akash.language,akash.salary)

rohan =employ()
rohan.name="Akash"          #This is an instant attribute 
print(rohan.name,rohan.language,rohan.salary)


#Heere name is object,instance attribute and salary,language are class attribute as they directly belong to the class  

